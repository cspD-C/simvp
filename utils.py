"""
utils.py - 工具函数集合

包含训练过程中使用的辅助函数:
1. set_seed: 设置随机种子,确保实验可复现
2. print_log: 同时打印到控制台和日志文件
3. output_namespace: 格式化输出配置参数
4. check_dir: 检查并创建目录
"""

import os
import logging
import torch
import random
import numpy as np
import torch.backends.cudnn as cudnn

# ============================================================================
# 函数1: set_seed - 设置随机种子
# ============================================================================
# 作用: 设置所有随机数生成器的种子,确保实验结果可复现
# 影响范围:
#   - Python random 模块
#   - NumPy 随机数生成
#   - PyTorch 随机数生成
#   - cuDNN 确定性行为
# ============================================================================

def set_seed(seed):
    random.seed(seed)
    np.random.seed(seed)
    torch.manual_seed(seed)
    cudnn.deterministic = True

# ============================================================================
# 函数2: print_log - 打印并记录日志
# ============================================================================
# 作用: 同时将信息打印到控制台和写入日志文件
# 用途: 记录训练过程中的重要信息(损失、指标等)
# ============================================================================

def print_log(message):
    print(message)
    logging.info(message)

# ============================================================================
# 函数3: output_namespace - 格式化输出配置
# ============================================================================
# 作用: 将 argparse 的 Namespace 对象格式化为易读的字符串
# 用途: 在训练开始时打印所有配置参数
# ============================================================================

def output_namespace(namespace):
    configs = namespace.__dict__
    message = ''
    for k, v in configs.items():
        message += '\n' + k + ': \t' + str(v) + '\t'
    return message

# ============================================================================
# 函数4: check_dir - 检查并创建目录
# ============================================================================
# 作用: 如果目录不存在则创建,用于创建结果保存目录
# ============================================================================

def check_dir(path):
    if not os.path.exists(path):
        os.makedirs(path)